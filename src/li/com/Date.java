package li.com;

public class Date {
	private int day;
	private int month;
	private int year;
	
	// getter for day
	public int getDay() {
		return day;
	}
	
	// setter for day
	public void setDay(int day) {
		this.day = day;
	}
	
	// getter for month
	public int getMonth() {
		return month;
	}
	
	// setter for month
	public void setMonth(int month) {
		this.month = month;
	}
	
	// getter for year
	public int getYear() {
		return year;
	}
	
	// setter for year
	public void setYear(int year) {
		this.year = year;
	}
}
